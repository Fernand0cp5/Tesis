function Salida=fd(Entrada)
xin=Entrada(4);
Gsin=Entrada(14);
Gsout=Entrada(15);
rocm=Entrada(16);
vol=Entrada(17);
p(1)=Entrada(18);
p(2)=Entrada(19);
p(3)=Entrada(20);
P=Entrada(21);
Hk=rocm*vol;
k0b=87.7457696248338;
k0c=141.029744964245;
k0d=71.4294606149581;
Eb=1.40605122040969;
Ec=1.85185098278557;
Ed=1.31155513877292;
x(1)=k0b*exp(-Eb/P);
x(2)=k0c*exp(-Ec/P);
x(3)=k0d*exp(-Ed/P);
xd=(Gsin*xin-Gsout*p(3)+Hk*p(1)*x(1)*0.1084+Hk*p(2)*x(2)*0.7474-Hk*p(3)*x(3))/Hk;
Salida=xd;
end