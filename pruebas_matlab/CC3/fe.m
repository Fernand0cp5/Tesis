function Salida=fe(Entrada)
xin=Entrada(5);
Gsin=Entrada(14);
Gsout=Entrada(15);
rocm=Entrada(16);
vol=Entrada(17);
p(1)=Entrada(18);
p(2)=Entrada(19);
p(3)=Entrada(20);
p(4)=Entrada(21);
P=Entrada(22);
Hk=rocm*vol;
k0b=87.7457696248338;
k0c=141.029744964245;
k0d=71.4294606149581;
k0e=41.3066717870455;
Eb=1.40605122040969;
Ec=1.85185098278557;
Ed=1.31155513877292;
Ee=1.18881489358611;
x(1)=k0b*exp(-Eb/P);
x(2)=k0c*exp(-Ec/P);
x(3)=k0d*exp(-Ed/P);
x(4)=k0e*exp(-Ee/P);
xe=(Gsin*xin-Gsout*p(4)+Hk*p(1)*x(1)*0.0216+Hk*p(2)*x(2)*0.1084+Hk*p(3)*x(3)*0.7474-Hk*p(4)*x(4))/Hk;
Salida=xe;
end